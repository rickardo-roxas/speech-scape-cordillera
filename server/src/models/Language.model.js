/**
 * Class representing a language.
 */
class Language {
    /**
     * Creates a new Language object.
     * @param {String} name - Name of the language.
     * @param {float} percentage - Percentage of usage. 
     */
    constructor(name, percentage) {
        this.name = name;
        this.percentage = percentage;
    }
}

export default {
    Language
};
